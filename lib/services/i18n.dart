import 'package:flutter/foundation.dart';
import 'package:shared_preferences/shared_preferences.dart';

import 'telegram.dart';

/// Lightweight i18n with two backends: a built-in translation map for `zh`
/// and `en`, plus a persisted user override (Profile → 语言 / Language).
///
/// Resolution order (highest to lowest):
///   1. User-saved override (SharedPreferences key `app.locale`)
///   2. Telegram WebApp `language_code` (e.g. "zh", "en-US", "ru")
///   3. Browser `navigator.language` (forwarded by index.html)
///   4. Default 'zh'
class I18n extends ChangeNotifier {
  I18n._();
  static final I18n instance = I18n._();

  static const _prefsKey = 'app.locale';
  static const supported = [
    'zh', 'en', 'ru', 'es', 'ar', 'fa', 'hi', 'id',
    'ja', 'ko', 'pt', 'tr', 'vi', 'fr', 'de', 'it',
  ];

  String _locale = 'zh';
  String get locale => _locale;
  bool _userOverride = false;
  bool get userOverride => _userOverride;

  /// Raw `language_code` value reported by Telegram (or browser fallback) on
  /// the most recent initialize() call. Used by the picker to surface the
  /// auto-detected language so the user can see what "follow Telegram" maps
  /// to right now.
  String _detectedRaw = '';
  String get detectedRaw => _detectedRaw;
  String get detectedLocale => _normalize(_detectedRaw.toLowerCase());

  Future<void> initialize() async {
    final prefs = await SharedPreferences.getInstance();
    final saved = prefs.getString(_prefsKey);
    _detectedRaw = Telegram.languageCode();
    if (saved != null && supported.contains(saved)) {
      _locale = saved;
      _userOverride = true;
      notifyListeners();
      return;
    }
    _locale = _normalize(_detectedRaw.toLowerCase());
    notifyListeners();
  }

  Future<void> setLocale(String code) async {
    final c = _normalize(code);
    if (!supported.contains(c)) return;
    _locale = c;
    _userOverride = true;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_prefsKey, c);
    notifyListeners();
  }

  /// Clear user override → fall back to Telegram-detected language.
  Future<void> resetToAuto() async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.remove(_prefsKey);
    _userOverride = false;
    _detectedRaw = Telegram.languageCode();
    _locale = _normalize(_detectedRaw.toLowerCase());
    notifyListeners();
  }

  String _normalize(String code) {
    if (code.isEmpty) return 'en';
    final lower = code.toLowerCase();
    // Chinese (Simplified or Traditional) → 'zh'
    if (lower.startsWith('zh')) return 'zh';
    // Portuguese variants (pt, pt-br, pt-pt) → 'pt'
    if (lower.startsWith('pt')) return 'pt';
    // Direct prefix matches against supported list
    for (final c in const [
      'en', 'ru', 'es', 'ar', 'fa', 'hi', 'id',
      'ja', 'ko', 'tr', 'vi', 'fr', 'de', 'it',
    ]) {
      if (lower.startsWith(c)) return c;
    }
    return 'en'; // fallback: English (more universal than Chinese)
  }

  /// Translate a key. Resolution: requested locale → English → Chinese → key.
  /// English is the canonical fallback; Chinese is final safety net since the
  /// app was authored in Chinese first.
  String t(String key) {
    final m = _maps[_locale] ?? _maps['en']!;
    return m[key] ?? _maps['en']?[key] ?? _maps['zh']?[key] ?? key;
  }
}

